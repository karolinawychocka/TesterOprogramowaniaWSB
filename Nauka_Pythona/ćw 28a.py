#str.format()


name = "Karolina"

print("Hello my name is {}".format(name))
print("Hello my name is {:20} Nice to meet you!".format(name))
print("Hello my name is {:<20} Nice to meet you!".format(name))
print("Hello my name is {:>20} Nice to meet you!".format(name))
print("Hello my name is {:^20} Nice to meet you!".format(name))